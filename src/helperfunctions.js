// create a shuffle method to randomize answer choices
// use the Fischer-Yates shuffle algorithm because it is truly random
export const createChoices = (array) => {
  for (let i = array.length - 1; i > 0; i--) {
    let j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
  return array;
};
